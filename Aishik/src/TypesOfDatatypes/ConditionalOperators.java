package TypesOfDatatypes;

public class ConditionalOperators {
	public static void main(String[] args) {
		double a=10.0;
		int b=10;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}
}
